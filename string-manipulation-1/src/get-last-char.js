/* 1. Define the function.
2. Create a container that will store the last character value
3. Set the container = to the value of the string[string.length - 1] to find the
last character of the string.
4. Return container from the function. */

function getLastChar(string) {
  var lastChar = string[string.length - 1];
  return lastChar;
}
